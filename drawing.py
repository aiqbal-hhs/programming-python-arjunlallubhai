import turtle
timmy = turtle.Turtle()
timmy.color("purple")
timmy.pensize(10)

timmy.penup()
timmy.goto(15, 70)
timmy.pendown()


timmy.fillcolor("yellow")
timmy.begin_fill()
timmy.forward(200)
timmy.left(90)
timmy.forward(200)
timmy.left(90)
timmy.forward(200)
timmy.left(90)
timmy.forward(200)
timmy.left(90)
timmy.end_fill()



timmy.penup()
timmy.goto(20, 275)
timmy.pendown()

timmy.color("purple")
timmy.pensize(10)

timmy.fillcolor("red")
timmy.begin_fill()
